// CÓDIGO NÃO EXECUTÁVEL

// Procedural
processamento(valor1, valor2, valor3)

// OO
objeto = {
    valor1,
    valor2,
    valor3,
    processamento() {
        // ...
    }
}

objeto.processamento() // Foco passou a ser o objeto

// Principais pilares da OO
// 1. Abstração: representação do Objeto do mundo real para o Sistema
// 2. Encapsulamento: detalhes de implementação protegidos/escondidos
// 3. Herança (prototype)
// 4. Polimorfismo