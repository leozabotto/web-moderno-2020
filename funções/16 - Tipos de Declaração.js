// function declaration
function soma(x, y) {
    return x + y
}

// function expression - só pode ser chamada depois de sua 
// declaração
const sub = function (x, y) {
    return x - y
}

// named function expression - só pode ser chamada depois de sua 
// declaração
const mult = function mult(x, y) {
    return x * y
}
